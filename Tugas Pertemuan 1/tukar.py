# Fungsi menukar posisi manggis dan pisang
def tukar(x, y, z):
    piring1 = y
    piring2 = x

    print("\nSetelah ditukar, menjadi")
    print("Piring 1: ", piring1)
    print("Piring 2: ", piring2)
    print("Piring 3: ", z)


piring1 = "Manggis"
piring2 = "Pisang"
piring3 = "Kosong"
print("Piring 1: ", piring1)
print("Piring 2: ", piring2)
print("Piring 3: ", piring3)

tukar(piring1, piring2, piring3)
